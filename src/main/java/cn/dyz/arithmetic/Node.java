package cn.dyz.arithmetic;

public class Node {

	private int value;
	private Node left;
	private Node rgiht;
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public Node getLeft() {
		return left;
	}
	public void setLeft(Node left) {
		this.left = left;
	}
	public Node getRgiht() {
		return rgiht;
	}
	public void setRgiht(Node rgiht) {
		this.rgiht = rgiht;
	}
	
	
}
